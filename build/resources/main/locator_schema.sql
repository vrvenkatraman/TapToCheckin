CREATE TABLE IF NOT EXISTS USER_DETAILS (
  EMP_ID         VARCHAR2(20) NOT NULL,
  NAME		      VARCHAR2(32) NOT NULL,
  EMAIL			    VARCHAR2(200) NOT NULL,
  MOBILE_NUMBER	VARCHAR2(90) NOT NULL,
  ROLE			    VARCHAR2(30) NOT NULL,
  TEAM			    VARCHAR2(50) NOT NULL,
  PROFILE_PIC		BLOB NULL,
  CONSTRAINT PK_USER_DETAILS_EMP_ID PRIMARY KEY(EMP_ID)
);

CREATE TABLE IF NOT EXISTS LOCATION_INFO (
  LOCATION_ID		VARCHAR2(32) NOT NULL,
  FLOOR_PLAN		BLOB  NULL
  CONSTRAINT PK_LOCATION_INFO_LOC_ID PRIMARY KEY(LOCATION_ID)
);

CREATE TABLE IF NOT EXISTS CHECKIN_INFO (
  EMP_ID         VARCHAR2(32) NOT NULL,
  LOCATION_ID   VARCHAR2(32) NOT NULL,
);
